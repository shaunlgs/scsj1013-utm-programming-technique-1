#include <iomanip>
using namespace std;

int main()
{
    int j;
    cout << "\n\t Multiplication Tables";
    cout << "\n _________________________________________\n\n";
    for (int n=2; n<=12; n++)
    {
        j=1;
        while (j<=12)
        {
         cout << j << " x " << n << " = " << j*n << endl;
         j++;            
        }
        cout << endl;                
    }    
    
    cout << "\n\t End of Program \n";
    
    system("pause");
    return 0;
}

